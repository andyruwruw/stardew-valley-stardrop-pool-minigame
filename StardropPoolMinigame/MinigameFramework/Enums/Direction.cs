namespace MinigameFramework.Enums
{
    /// <summary>
    /// Cardinal and ordinal directions.
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// Up.
        /// </summary>
        North = 0,

        /// <summary>
        /// Diagonally up and right.
        /// </summary>
        NorthEast = 1,

        /// <summary>
        /// Right.
        /// </summary>
        East = 2,

        /// <summary>
        /// Diagonally down and right.
        /// </summary>
        SouthEast = 3,

        /// <summary>
        /// Down.
        /// </summary>
        South = 4,

        /// <summary>
        /// Diagonally down and left.
        /// </summary>
        SouthWest = 5,

        /// <summary>
        /// Left.
        /// </summary>
        West = 6,

        /// <summary>
        /// Diagonally up and left.
        /// </summary>
        NorthWest = 7,
    }
}
